# Написати рекурсивну функцію знаходження ступеня числа.

def power(base, exponent):

    if exponent == 0:  # Випадок коли показник ступеня дорівнює 0 повертаємо 1
        return 1
    else:
        return base * power(base, exponent - 1)  # Рекурсивний виклик функції зі зменшеним на одиницю показником

# Приклад використання:
result = power(50, 3)  # Очікуваний результат 2 у 3 степені = 8
print(result)